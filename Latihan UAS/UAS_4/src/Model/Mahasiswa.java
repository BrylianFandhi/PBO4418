package Model;

/**
 *
 * @author bryli
 */
public class Mahasiswa {
    private float uts, uas, akhir;
    private String nim, nama;
    /**
     * @return the uts
     */
    public float getUts() {
        return uts;
    }

    /**
     * @param uts the uts to set
     */
    public void setUts(float uts) {
        this.uts = uts;
    }

    /**
     * @return the uas
     */
    public float getUas() {
        return uas;
    }

    /**
     * @param uas the uas to set
     */
    public void setUas(float uas) {
        this.uas = uas;
    }

    /**
     * @return the akhir
     */
    public float getAkhir() {
        return akhir;
    }

    /**
     * @param akhir the akhir to set
     */
    public void setAkhir(float akhir) {
        this.akhir = akhir;
    }

    /**
     * @return the nim
     */
    public String getNim() {
        return nim;
    }

    /**
     * @param nim the nim to set
     */
    public void setNim(String nim) {
        this.nim = nim;
    }

    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }
    
}
